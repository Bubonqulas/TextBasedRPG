public class Object {

    private String name;
    private int excuseBlocker;

    // CONSTRUCTOR

    Object(String name, int excuseBlocker) {

        this.name = name;
        this.excuseBlocker = excuseBlocker;
    }

    public String getName() {
        return name;
    }

    public int getExcuseBlocker() {
        return excuseBlocker;
    }

}
